function updatePageLanguage(lang) {
    // Update HTML attributes
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    // Save preference
    localStorage.setItem('Rawasi-lang', lang);

    // Update all translatable elements
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.value = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Update meta description
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && translations[lang] && translations[lang]["meta_description"]) {
        metaDesc.content = translations[lang]["meta_description"];
    }

    // Update page title
    if (translations[lang] && translations[lang]["page_title"]) {
        document.title = translations[lang]["page_title"];
    }
}

// Initialize language on load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('Rawasi-lang') || 'en';
    updatePageLanguage(savedLang);
});